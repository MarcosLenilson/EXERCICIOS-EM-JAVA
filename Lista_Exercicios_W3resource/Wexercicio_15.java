package Lista_Exercicios_W3resource;

public class Wexercicio_15 {

    public static void main(String[] args) {

        int a = 20;
        int b = 50;
        int aux;

        System.out.println("Before swapping : a, b = "+a+", "+ + b);
        aux = a;
        a = b;
        b = aux;
        System.out.println("After swapping : a, b = "+a+", "+ + b);
    }
}
